#include "libft.h"

char	*ft_strzero(char *s, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if (!(s[i]))
			return (s);
		i++;
	}
	s[i - 1] = '\0';
	return (s);
}
