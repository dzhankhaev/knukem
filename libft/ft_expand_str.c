#include "libft.h"

void	ft_expand_str(int a, char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 33 && str[i] <= 127)
			i++;
		if (str[i] >= 1 && str[i] <= 32)
			i = ft_skip(i, str);
	}
}
