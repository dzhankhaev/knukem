#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int un;

	un = 0;
	if (n < 0)
	{
		ft_putchar('-');
		un = n * -1;
	}
	else
		un = n;
	if ((un / 10) != 0)
		ft_putnbr((un / 10));
	ft_putchar((un % 10) + '0');
}
